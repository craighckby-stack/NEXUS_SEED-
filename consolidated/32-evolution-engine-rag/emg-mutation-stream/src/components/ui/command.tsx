// --- Component Definitions ---

const Command = React.forwardRef<
  React.ElementRef<typeof CommandPrimitive>,
  React.ComponentPropsWithoutRef<typeof CommandPrimitive>
>(({ className, ...props }, ref) => (
  <CommandPrimitive
    ref={ref}
    data-slot="command"
    className={cn(
      "bg-popover text-popover-foreground flex h-full w-full flex-col overflow-hidden rounded-md",
      className
    )}
    {...props}
  />
))
Command.displayName = "Command"

interface CommandDialogProps
  extends React.ComponentProps<typeof Dialog> {
  title?: string
  description?: string
  className?: string
  showCloseButton?: boolean
}

function CommandDialog({
  title = "Command Palette",
  description = "Search for a command to run...",
  children,
  className,
  showCloseButton = true,
  ...props
}: CommandDialogProps) {
  return (
    <Dialog {...props}>
      <DialogHeader className="sr-only">
        <DialogTitle>{title}</DialogTitle>
        <DialogDescription>{description}</DialogDescription>
      </DialogHeader>
      <DialogContent
        className={cn("overflow-hidden p-0 shadow-lg", className)}
        showCloseButton={showCloseButton}
      >
        <Command
          className={cn(
            // General Grouping and Heading styles
            "[&_[cmdk-group-heading]]:text-muted-foreground",
            "[&_[cmdk-group-heading]]:px-2",
            "[&_[cmdk-group-heading]]:font-medium",
            "[&_[cmdk-group]]:px-2",
            "[&_[cmdk-group]:not([hidden])~[cmdk-group]]:pt-0",

            // Dialog Specific Input Overrides (increase size for dialog aesthetic)
            // Fix input wrapper height to h-12
            "[&_[data-slot=command-input-wrapper]]:h-12", 
            // Fix input height to h-12
            "[&_[data-slot=command-input]]:h-12",
            // Fix item padding for dialog aesthetic
            "[&_[data-slot=command-item]]:py-3",
            
            // Icon sizing overrides (size-5 = h-5 w-5)
            "[&_[data-slot=command-input-wrapper]_svg]:size-5",
            "[&_[data-slot=command-item]_svg]:size-5"
          )}
        >
          {children}
        </Command>
      </DialogContent>
    </Dialog>
  )
}

