from typing import List, Dict, Set
import re

# --- Context Retrieval Layer ---

def _get_keywords(text: str) -> Set[str]:
    """Utility to extract clean keywords from text, supporting basic tokenization.
    Ignores common noise and non-alphanumeric characters.
    """
    # Use regex to find word boundaries and exclude short noise words (e.g., single letters)
    return set(re.findall(r'\b[a-z]{2,}\b', text.lower()))

def get_relevant_context(current_query: str, history: List[Dict], max_interactions: int = 3, min_keyword_overlap: int = 1) -> List[Dict]:
    """
    Retrieves relevant interaction history based on keyword overlap and recency.
    Prioritizes recent interactions that share common keywords with the current query.

    Args:
    - current_query (str): The input query.
    - history (List[Dict]): Interaction history.
    - max_interactions (int): Maximum interactions to return.
    - min_keyword_overlap (int): Minimum number of shared keywords required for inclusion.

    Returns:
    - List[Dict]: Relevant interaction context.
    """
    query_keywords = _get_keywords(current_query)
    relevant_context = []

    # Iterate backwards (recency priority)
    for interaction in reversed(history):
        history_input = interaction.get("user_input", "")
        if not history_input:
            continue
        
        history_keywords = _get_keywords(history_input)
        
        # Calculate overlap
        overlap = len(query_keywords.intersection(history_keywords))
        
        if overlap >= min_keyword_overlap:
            relevant_context.append(interaction)
        
        if len(relevant_context) >= max_interactions:
            break
            
    return relevant_context

