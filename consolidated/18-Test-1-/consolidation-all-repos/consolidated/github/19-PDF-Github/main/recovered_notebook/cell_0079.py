def submit_search_tasks(engines_to_use: List[str], topics: List[str]) -> Dict[str, List[Tuple[str, List[str]]]]:
    """
    Submits search tasks in parallel using multiple engines for each topic.
    Results are structured explicitly by topic and source engine.

    Args:
        engines_to_use (List[str]): List of search engines to use.
        topics (List[str]): List of topics to search for.

    Returns:
        Dict[str, List[Tuple[str, List[str]]]]:
            A dictionary mapping topic -> list of (engine_name, search_results_list).
    """
    results: Dict[str, List[Tuple[str, List[str]]]] = {}
    try:
        with concurrent.futures.ThreadPoolExecutor(max_workers=MAX_WORKERS) as executor:
            futures = {
                executor.submit(search, engine, topic): (engine, topic)
                for engine in engines_to_use
                for topic in topics
            }

            logging.info(f"Submitted {len(futures)} concurrent search tasks.")

            for future in concurrent.futures.as_completed(futures):
                engine, topic = futures[future]
                try:
                    # Set a conservative timeout for simulation
                    search_results = future.result(timeout=15)
                    results.setdefault(topic, []).append((engine, search_results))
                    logging.debug(f"Collected results for {topic} from {engine}")
                except concurrent.futures.TimeoutError:
                    logging.warning(f"Search on {topic} from {engine} timed out after 15s")
                except Exception as e:
                    logging.error(f"Error during search on {topic} from {engine}: {e}")
            return results
    except Exception as e:
        logging.critical(f"Critical failure in ThreadPool execution: {e}")
        return {}

