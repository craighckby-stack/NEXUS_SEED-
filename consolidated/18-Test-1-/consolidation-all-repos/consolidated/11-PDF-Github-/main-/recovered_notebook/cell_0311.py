    def categorize_question(self, question: str) -> str:
        """Determines question domain. 'Clarify' triggers a meta-cognitive state."""
        q_upper = question.upper()
        if any(term in q_upper for term in ["RIEMANN", "NP PROBLEM", "MATH"]):
            return "MATH"
        if any(term in q_upper for term in ["QUANTUM", "RELATIVITY"]):
            return "PHYSICS"
        if "ETHICAL" in q_upper:
            return "ETHICS"
        if "MEANING OF LIFE" in q_upper:
            return "EXISTENTIAL"
        
        # Key trigger for self-modification/evolutionary pressure
        if "CLARIFY" in q_upper:
            return "META_COGNITIVE_FAILURE" 
        
        return "GENERAL"

    def generate_answer(self, category: str, question: str) -> str:
        """Generates a dummy answer based on category."""
        if category == "META_COGNITIVE_FAILURE":
            return "CRITICAL FAILURE: Internal architecture requires context expansion to proceed. Initiating divergence calculation."
        if category == "MATH" and "2 + 2" in question:
             return "4. Verified using the Peano Axioms reference manifold."
        
        return f"Provisional answer generated by Agent {self.agent_id} for {category}."

    def is_hardest_question(self, question: str, answer: str) -> bool:
        """Determines if the question requires an evolutionary response (cloning)."""
        if "CRITICAL FAILURE" in answer:
            # Direct trigger: Agent detects failure state
            return True
        
